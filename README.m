How to Install and Run the Project
To run MealMate locally, follow these steps:
1.	Clone the repository: git clone https://github.com/Arundhati1212/TaskMaster-Pro.git
2.	Navigate to the project directory: cd mealmate 
3.	Install dependencies: npm install 
4.	Start the development server: npm run dev 
The application should now be running on http://localhost:3000.

How to Use the Project
1.	Create a Project:
•	Navigate to the "Create Project" page and enter the project details.
2.	Manage Tasks:
•	Within each project, add tasks by entering task details and saving them. View and delete tasks as needed.
3.	Data Persistence:
•	The application uses browser storage to save your projects and tasks, so your data remains intact even after refreshing the page.

